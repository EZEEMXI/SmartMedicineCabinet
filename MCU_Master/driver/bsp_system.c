#include "bsp_system.h"

/*==========================================================
 * @function    BSP_DelayUs
 * @brief       微秒延时（72MHz下，循环9次≈1us）
 *==========================================================*/
void BSP_DelayUs(uint32_t us)
{
    uint32_t i;
    for (; us > 0; us--)
    {
        i = 9;
        while (i--) __NOP();
    }
}
